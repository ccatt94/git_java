
public class RelationalOp {

	public static void main(String[] args) {
		System.out.println("3 >= 2 : " + (3 >= 2));
		System.out.println("3 <= 2 : " + (3 <= 2));
		
		double num1 = 7.0;
		int num2 = 7;
		
		System.out.println("7.0 == 7 : " + (num1 == num2));
		System.out.println("7.0 != 7 : " + (num1 != num2));
	}

}
