
public class SwitchExample {

	public static void main(String[] args) {
		
		
		int num = 1;
		
		switch (num) {
		case 1: 
			System.out.println("1입니다.");
			System.out.println("1입니다.");
			
		case 2: 
			System.out.println("2입니다.");
			System.out.println("2입니다.");
			break;
		default:
			System.out.println("디폴트 입니다.");
			System.out.println("디폴트 입니다.");
		
		}
		System.out.println("프로그램 종료 합니다.");
		
		
	}

}
